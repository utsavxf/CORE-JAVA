public class FourPillars_4 {
/*
Encapsulation:
->Bundling data (attributes) and methods (functions) that operate on the data within a single unit (class).
->Protecting data from unauthorized access by using access modifiers like private.
->Example: A Car class encapsulates attributes like color, model, and methods like startEngine(), accelerate().

Inheritance:
->Creating new classes (subclasses) from existing classes (superclasses).
->Subclasses inherit properties and behaviors from their superclass.
->Promotes code re-usability and a hierarchical structure.
->Example: A SportsCar class can inherit from a Car class, inheriting common properties and adding specific features.

Polymorphism:
->The ability of an object to take on many forms.
->Method Overloading: Defining methods with the same name but different parameters within the same class.
->Method Overriding: Redefining a method in a subclass with the same name and parameters as in the superclass.
->Example: A Shape class can have a calculateArea() method, and its subclasses like Circle, Rectangle, and Triangle can override this method to calculate their specific areas.

Abstraction:
->Hiding implementation details and showing only the essential features to the user.
->Abstract classes and interfaces are used to achieve abstraction.
->Example: A RemoteControl interface can define methods like powerOn(), changeChannel(), without specifying the exact implementation.
*/
}
